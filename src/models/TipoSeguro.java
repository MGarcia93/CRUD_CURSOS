package models;

public class TipoSeguro {
	
	private int id;
	private String Descripcion;
	 
	public TipoSeguro(){
		
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescripcion() {
		return Descripcion;
	}
	public void setDescripcion(String descripcion) {
		Descripcion = descripcion;
	}
	

}
